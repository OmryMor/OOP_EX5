package ex5.utils;

/**
 * This enum represents the different types of content that a line can have.
 */
public enum LineContent {
    CLOSE_BRACKET,
    METHOD_DECLARATION,
    VARIABLE_DECLARATION,
    IF_STATEMENT,
    WHILE_STATEMENT,
    VAR_ASSIGNMENT,
    RETURN,
    METHOD_CALL,
    ILLEGAL
}
